class ServicePackage {
	private String service;
	private long value;
	
	public String getService() {
		return service;
	}
	
	public long getValue() {
		return value;
	}
	
	public void setService(String service) {
		this.service = service;
	}
	
	public void setValue(long value) {
		this.value = value;
	}
}
