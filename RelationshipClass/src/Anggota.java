//--------- Aggregation ----------//

// Class anggota
public class Anggota {
	private String nama;
	private int id_anggota;
	
	public String getAnggota() {
		return nama;
	}
	
	public int getId() {
		return id_anggota;
	}
	
	public void setAnggota(String name) {
		nama = name;
	}
	
	public void setId(int no_id) {
		id_anggota = no_id;
	}
}
